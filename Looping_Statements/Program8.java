package Looping_Statements;

public class Program8 {
    public static void main(String[] args) {
        for (int i=1;i<=9;i++)
        {
            System.out.print(i);
        }
        System.out.println();
        System.out.println("---------------");
        for (int i=9;i>=1;i--)
        {
            System.out.print(i);
        }
    }
}
